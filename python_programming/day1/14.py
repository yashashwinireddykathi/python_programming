#operators
def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def mul(a,b):
    return a*b
def div(a,b):
    return a/b
def rem(a,b):
    return a%b
def exp(a,b):
    return a**b
print(add(10,20))
print(sub(10,20))
print(mul(10,20))
print(div(10,20))
print(rem(10,20))
print(exp(10,20))